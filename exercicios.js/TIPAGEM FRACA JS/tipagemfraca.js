let teste = 'qualquer';  // Declara uma variável chamada 'teste' e atribui a ela o valor 'qualquer', que é um texto (string).
console.log(teste);  // Exibe o valor da variável 'teste' no console, que será 'qualquer'.
console.log(typeof teste);  // Exibe o tipo do valor armazenado na variável 'teste'. Nesse caso, será 'string', já que 'qualquer' é um texto.

teste = 1234;  // A variável 'teste' agora recebe um novo valor: 1234, que é um número.
console.log(teste);  // Exibe o novo valor da variável 'teste' no console, que será 1234.
console.log(typeof teste);  // Exibe o tipo do valor armazenado na variável 'teste'. Agora, será 'number', porque 1234 é um número.
